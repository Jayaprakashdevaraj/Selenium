package week1.day2;

public class CSCalculator {
	
	public int AddThreeNo(int n1,int n2,int n3) {
		int sum = n1+n2+n3;
		return sum;
	}
	
	public int SubTwoNo(int n1,int n2) {
		int sub = n1+n2;
		return sub;
	}	
	public double MulTwoNo(double n1,double n2) {
		double mul = n1*n2;
		return mul;
	}	
	public float divTwoNo(float n1,float n2) {
		float div = n1/n2;
		return div;
	}	
		
	

}
